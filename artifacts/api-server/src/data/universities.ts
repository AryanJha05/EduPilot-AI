export function ieltsEquivalent(test: string, score: number | null): number | null {
  if (!score) return null;
  if (test === "IELTS") return score;
  if (test === "TOEFL") return Math.round((score / 120) * 9 * 2) / 2;
  if (test === "Duolingo") return Math.round((score / 160) * 9 * 2) / 2;
  return null;
}
